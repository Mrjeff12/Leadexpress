/**
 * Fuzzy-search cities by name across all states.
 * Matches if the query appears anywhere in the city name (case-insensitive).
 */
export function findCityByName(query: string): CityInfo[] {
  const q = query.toLowerCase().trim();
  const results: CityInfo[] = [];

  for (const state of Object.values(CITY_ZIPS)) {
    for (const info of Object.values(state)) {
      if (info.city.toLowerCase().includes(q)) {
        results.push(info);
      }
    }
  }

  return results;
}

/**
 * Returns the list of states available in CITY_ZIPS, formatted for UI buttons.
 */
export function getStateOptions(): { id: string; label: string; emoji: string }[] {
  return Object.keys(CITY_ZIPS).map((stateCode) => ({
    id: stateCode,
    label: STATE_META[stateCode]?.label ?? stateCode,
    emoji: STATE_META[stateCode]?.emoji ?? "",
  }));
}

/**
 * Returns the list of cities for a given state code, formatted for UI buttons.
 */
export function getCitiesByState(
  state: string,
): { id: string; label: string }[] {
  const stateData = CITY_ZIPS[state.toUpperCase()];
  if (!stateData) return [];

  return Object.entries(stateData).map(([key, info]) => ({
    id: key,
    label: info.city,
  }));
}

/**
 * Returns a de-duplicated, sorted array of all ZIP codes for the given cities
 * within a state.
 */
export function getAllZipsForCities(
  state: string,
  cityKeys: string[],
): string[] {
  const stateData = CITY_ZIPS[state.toUpperCase()];
  if (!stateData) return [];

  const zipSet = new Set<string>();

  for (const key of cityKeys) {
    const info = stateData[key.toLowerCase()];
    if (info) {
      for (const zip of info.zips) {
        zipSet.add(zip);
      }
    }
  }

  return Array.from(zipSet).sort();
}
